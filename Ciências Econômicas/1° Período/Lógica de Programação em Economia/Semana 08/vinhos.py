contador_vinhos = 0
ct = 0
cb = 0
cr = 0

while True:
    tv = input('Digite o tipo de vinho: ')
    if tv == 'T':
        ct = ct + 1
    if tv == 'B':
        cb = cb + 1
    if tv == 'R':
        cr = cr + 1
    if tv == 'F':
        break

    contador_vinhos = contador_vinhos + 1

print('Percentual de vinho tinto = ', ct / contador_vinhos)
print('Percentual de vinho branco = ', cb / contador_vinhos)
print('Percentual de vinho rose = ', cr / contador_vinhos)




